package com.bingo.dao;

public class SlipInfoResponse {

    private int row;
    private int col;
    private String id;

    public SlipInfoResponse() {
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getCol() {
        return col;
    }

    public void setCol(int col) {
        this.col = col;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

}
